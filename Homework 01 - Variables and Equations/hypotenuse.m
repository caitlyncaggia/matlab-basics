function [hyp] = hypotenuse(sideA, sideB)
hyp = sqrt((sideA^2) + (sideB^2))
end

%Since the pythagorean theorem says that the hypotenuse squared is equal to
%the sum of each of the other sides squared, the hypotenuse is also equal to the
%square root of the sum of each of the other sides squared.

